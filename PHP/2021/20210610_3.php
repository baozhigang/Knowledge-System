<?php
/**
 * 常见的文件函数
 */

copy(); // 拷贝文件
dirname(); // 返回路径中的目录部分
basename(); // 返回路径中的文件名部分
pathinfo(); // 返回文件路径的信息
file_exists(); // 检查文件或目录是否存在
file_get_contents(); // 将整个文件读入一个字符串
file_put_contents(); // 将一个字符串写入文件
file(); // 把整个文件读入一个数组中
filesize(); // 取得文件大小
filetype(); // 取得文件类型
is_dir(); // 判断给定文件名是否是一个目录
is_file(); // 判断给定文件名是否为一个正常的文件
mkdir(); // 新建目录
rename(); // 重命名一个文件或目录
rmdir(); // 删除目录
touch(); // 设定文件的访问和修改时间
unlink(); // 删除文件

